/**
 * Склонение фразы «последний(е) N ключ(а/ей)» одновременно за оба слова.
 * Русский: количественное прилагательное «последний/последние» и
 * существительное «ключ/ключа/ключей» зависят от числа разными правилами,
 * литеральный плейсхолдер `(а/ей)` в готовом тексте грамматически невалиден.
 *
 * Английский: для count=1 — «1 last key», иначе «N last keys». Возвращаем
 * пару, чтобы вызывающая сторона подставила одну из локалей в окружающую
 * формулировку без повторной работы со склонением.
 */
export function pluralizeLastRefs(count: number): { ru: string; en: string } {
  if (count === 1) {
    return {
      ru: 'последний ключ',
      en: '1 last key',
    };
  }
  // Русские правила склонения существительного по последним цифрам числа:
  // - lastTwo 11..14 - «ключей» (особый случай, перебивает правило хвоста);
  // - lastOne 1 (вне 11..14) - «ключ» («21 ключ», «101 ключ»);
  // - lastOne 2..4 (вне 12..14) - «ключа» («22 ключа», «103 ключа»);
  // - остальные (0, 5..9) - «ключей».
  // Прилагательное «последние» одинаково для всех count > 1.
  const lastTwo = count % 100;
  const lastOne = count % 10;
  let word: 'ключ' | 'ключа' | 'ключей';
  if (lastTwo >= 11 && lastTwo <= 14) {
    word = 'ключей';
  } else if (lastOne === 1) {
    word = 'ключ';
  } else if (lastOne >= 2 && lastOne <= 4) {
    word = 'ключа';
  } else {
    word = 'ключей';
  }
  return {
    ru: `последние ${count} ${word}`,
    en: `${count} last keys`,
  };
}
